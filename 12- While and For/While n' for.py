""" A while is a certain loop that repeats itself as many times as the condition is true """

i = 0 #The variable that we will be using for the loop

while i != 5: #meanwhile "i" is different from 5, the following code will repeat itself
    print("i'll be printed five times")
    i+=1

""" A for statement is the same as while, but in a more complicated way, this way basically stores a variable with another"""

for variable_that_will_be_stored in "Hello World": #The "Hello "orld variable has 10 characters, so the loops will be 10 and the character will store everrey character
    print(variable_that_will_be_stored)

